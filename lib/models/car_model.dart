class Car {
  final String id;
  final String name;
  final String brand;
  final double price;
  final String imageUrl;
  final String transmission;
  final int seats;

  Car({
    required this.id,
    required this.name,
    required this.brand,
    required this.price,
    required this.imageUrl,
    required this.transmission,
    required this.seats,
  });
}